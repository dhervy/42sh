char	*ft_strcat(char *s1, const char *s2)
{
	int	c;
	int	i;

	c = 0;
	i = 0;
	while (s1[i])
		i++;
	while (s2[c])
	{
		s1[i] = s2[c];
		i++;
		c++;
	}
	s1[i] = 0;
	return (s1);
}
